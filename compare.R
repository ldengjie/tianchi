library("xts")
d<-read.csv("com.csv")
p1<-ts(d[1:30,1],fre=7)
p2<-ts(d[1:30,2],fre=7)
p3<-ts(d[1:30,3],fre=7)
p4<-ts(d[1:30,4],fre=7)
r1<-ts(d[32:61,1],fre=7)
r2<-ts(d[32:61,2],fre=7)
r3<-ts(d[32:61,3],fre=7)
r4<-ts(d[32:61,4],fre=7)
p1.xts<-xts(p1,seq(as.POSIXct("2014-09-01"),len=length(p1),by='day'))
p2.xts<-xts(p2,seq(as.POSIXct("2014-09-01"),len=length(p2),by='day'))
p3.xts<-xts(p3,seq(as.POSIXct("2014-09-01"),len=length(p3),by='day'))
p4.xts<-xts(p4,seq(as.POSIXct("2014-09-01"),len=length(p4),by='day'))
r1.xts<-xts(r1,seq(as.POSIXct("2014-09-01"),len=length(r1),by='day'))
r2.xts<-xts(r2,seq(as.POSIXct("2014-09-01"),len=length(r2),by='day'))
r3.xts<-xts(r3,seq(as.POSIXct("2014-09-01"),len=length(r3),by='day'))
r4.xts<-xts(r4,seq(as.POSIXct("2014-09-01"),len=length(r4),by='day'))
par(mfcol=c(2,1))
plot(as.zoo(cbind(p1.xts,p2.xts,p3.xts,p4.xts)),col=1:9,lty=1:9,screens=1)
legend(x="topleft",legend=c("Part1","NoTime","WithTime","moreFea"),lty=1:9,col=1:9)
plot(as.zoo(cbind(r1.xts,r2.xts,r3.xts,r4.xts)),col=1:9,lty=1:9,screens=1)
legend(x="topleft",legend=c("Part1","NoTime","WithTime","moreFea"),lty=1:9,col=1:9)
